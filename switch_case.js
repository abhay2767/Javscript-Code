//Demostrate the switch case statement
let age = 60;
switch(age)
{
    case 10:
        console.log("Your age is 10");
        break
    case 20:
        console.log("Your age is 20");
        break
    case 30:
        console.log("Your age is 30");
        break;
    case 40:
        console.log("Your age is 40");
        break;
    default:
        console.log("Your age is not match");
        break;
        
        
}